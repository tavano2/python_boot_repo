fruits = ["Apple", "Peach", "Pear"]
for fruit in fruits:
    print(fruit)

"""
반복문에서 제일 중요한 것은
동일한 줄의 코드를 여러 차례 수행하게 해준다는 것이다.

강의에서 지속적으로 강조하고 있지만
반복문 내부 코드는 들여쓰기로 구분하므로 들여쓰기 중요도에 대해 다시한번 생각하고 넘어가자.
"""

fruits = ["Apple", "Peach", "Pear"]
for fruit in fruits:
    print(fruit + " Pie")

